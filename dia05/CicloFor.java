package dia05;

public class CicloFor {
    public static void main(String[] args) {

        for (int i = 0; i <= 5; i += 2) {
            System.out.println("Número real " + i);
        }

        // Inversa

        for (int i = 5; i >= 1; i--) {
            System.out.println("Número inverso " + i);
        }

        //
        for (int i = 1; i <= 500; i++) {
            System.out.println(i + ".-  No debo hablar en clase");
        }
    }
}
