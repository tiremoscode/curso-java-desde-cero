package dia04;

public class OperadoresRelacionales {

    public static void main(String[] args) {
        int valor1 = 10;
        int valor2 = 5;

        System.out.println("Igualdad a==b " + (valor1 == valor2));
        System.out.println("Desigualdad a!=b " + (valor1 != valor2));
        System.out.println("Mayor que a>b " + (valor1 > valor2));
        System.out.println("Menor que a<b " + (valor1 < valor2));
        System.out.println("Mayor o igual que a >= b " + (valor1 >= valor2));
        System.out.println("Menor o igual que a <= b " + (valor1 <= valor2));
    }
}